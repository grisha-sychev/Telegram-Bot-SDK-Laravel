            File::deleteDirectory($downloadPath);
            mkdir($downloadPath, 0755, true);
            $this->line("  ‚úÖ –û—á–∏—â–µ–Ω–∞ –ø–∞–ø–∫–∞ –∑–∞–≥—Ä—É–∑–æ–∫");
        }

        if (is_dir($tempPath)) {
            File::deleteDirectory($tempPath);
            mkdir($tempPath, 0755, true);
            $this->line("  ‚úÖ –û—á–∏—â–µ–Ω–∞ –≤—Ä–µ–º–µ–Ω–Ω–∞—è –ø–∞–ø–∫–∞");
        }
    }

