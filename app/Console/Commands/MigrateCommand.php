<?php

namespace App\Console\Commands;

use Illuminate\Console\Command;
use Illuminate\Support\Facades\Http;
use Illuminate\Support\Facades\Storage;
use Illuminate\Support\Facades\File;
use App\Models\Bot;

class MigrateCommand extends Command
{
    protected $signature = 'bot:migrate 
                            {action : Action (export, import, clear, backup)}
                            {--format=json : Export format (json, csv)}
                            {--path= : File path for import/export}
                            {--force : Force action without confirmation}';
    
    protected $description = '–ú–∏–≥—Ä–∞—Ü–∏—è –¥–∞–Ω–Ω—ã—Ö –±–æ—Ç–æ–≤';

    public function handle()
    {
        $action = $this->argument('action');

        switch ($action) {
            case 'export':
                return $this->exportData();
            case 'import':
                return $this->importData();
            case 'clear':
                return $this->clearData();
            case 'backup':
                return $this->backupData();
            default:
                $this->error("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ –¥–µ–π—Å—Ç–≤–∏–µ: {$action}");
                $this->line('–î–æ—Å—Ç—É–ø–Ω—ã–µ –¥–µ–π—Å—Ç–≤–∏—è: export, import, clear, backup');
                return 1;
        }
    }

    private function exportData(): int
    {
        $this->info('üì§ –≠–∫—Å–ø–æ—Ä—Ç –¥–∞–Ω–Ω—ã—Ö –±–æ—Ç–æ–≤...');
        $this->newLine();

        $format = $this->option('format');
        $path = $this->option('path') ?? storage_path('app/bot_export_' . date('Y-m-d_H-i-s') . '.' . $format);

        // –°–æ–±–∏—Ä–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è —ç–∫—Å–ø–æ—Ä—Ç–∞
        $data = $this->collectExportData();

        try {
            if ($format === 'csv') {
                $this->exportToCsv($data, $path);
            } else {
                $this->exportToJson($data, $path);
            }

            $this->info("‚úÖ –î–∞–Ω–Ω—ã–µ —ç–∫—Å–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω—ã: {$path}");
            $this->line("üìä –ó–∞–ø–∏—Å–µ–π: " . $this->countRecords($data));
            $this->line("üíæ –†–∞–∑–º–µ—Ä: " . $this->formatFileSize(filesize($path)));

        } catch (\Exception $e) {
            $this->error("‚ùå –û—à–∏–±–∫–∞ —ç–∫—Å–ø–æ—Ä—Ç–∞: {$e->getMessage()}");
            return 1;
        }

        return 0;
    }

    private function importData(): int
    {
        $path = $this->option('path');
        
        if (!$path) {
            $path = $this->ask('–í–≤–µ–¥–∏—Ç–µ –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É –¥–ª—è –∏–º–ø–æ—Ä—Ç–∞');
        }

        if (!$path || !file_exists($path)) {
            $this->error('‚ùå –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω');
            return 1;
        }

        $this->info("üì• –ò–º–ø–æ—Ä—Ç –¥–∞–Ω–Ω—ã—Ö –∏–∑: {$path}");
        $this->newLine();

        if (!$this->option('force') && !$this->confirm('‚ö†Ô∏è  –ò–º–ø–æ—Ä—Ç –º–æ–∂–µ—Ç –ø–µ—Ä–µ–∑–∞–ø–∏—Å–∞—Ç—å —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –¥–∞–Ω–Ω—ã–µ. –ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å?', false)) {
            $this->info('–û—Ç–º–µ–Ω–µ–Ω–æ');
            return 0;
        }

        try {
            $extension = pathinfo($path, PATHINFO_EXTENSION);
            
            if ($extension === 'csv') {
                $data = $this->importFromCsv($path);
            } else {
                $data = $this->importFromJson($path);
            }

            $this->processImportData($data);

            $this->info('‚úÖ –ò–º–ø–æ—Ä—Ç –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ');
            $this->line("üìä –û–±—Ä–∞–±–æ—Ç–∞–Ω–æ –∑–∞–ø–∏—Å–µ–π: " . $this->countRecords($data));

        } catch (\Exception $e) {
            $this->error("‚ùå –û—à–∏–±–∫–∞ –∏–º–ø–æ—Ä—Ç–∞: {$e->getMessage()}");
            return 1;
        }

        return 0;
    }

    private function clearData(): int
    {
        $this->info('üóëÔ∏è  –û—á–∏—Å—Ç–∫–∞ –¥–∞–Ω–Ω—ã—Ö...');
        $this->newLine();

        if (!$this->option('force') && !$this->confirm('‚ö†Ô∏è  –≠—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ —É–¥–∞–ª–∏—Ç –≤—Å–µ –¥–∞–Ω–Ω—ã–µ. –ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å?', false)) {
            $this->info('–û—Ç–º–µ–Ω–µ–Ω–æ');
            return 0;
        }

        try {
            $this->clearFiles();
            $this->clearCache();
            $this->clearLogs();

